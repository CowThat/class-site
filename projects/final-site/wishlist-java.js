$('th').on('click', function(){
	var column = $(this).data('column')
	var order = $(this).data('order')
	var text = $(this).html()
	text = text.substring(0, text.length - 1)

	if(order == 'desc'){
		$(this).data('order', "asc")
		wishlistTable = wishlistTable.sort((a,b) => a[column] > b[column] ? 1 : -1)
		text += '&#9660'

	}else{
		$(this).data('order', "desc")
		wishlistTable = wishlistTable.sort((a,b) => a[column] < b[column] ? 1 : -1)
		text += '&#9650'

	}

	$(this).html(text)
	buildTable(wishlistTable)

})

buildTable(wishlistTable)

function buildTable(data){
  var table = document.getElementById('wishlistTable')

  table.innerHTML = ''

  for (var i = 0; i < data.length; i++){
    var row = `<tr>
                <td><img src=\"photos/wishlist/${data[i].img}\"></img></td>
                <td>${data[i].name}</td>
                <td>${data[i].animal}</td>
                <td>${data[i].sizeWanted}</td>
                <td>${data[i].color}</td>
                <td>${data[i].iso}</td>
              </tr>`
    table.innerHTML += row
  }
}



//bounce icons when the page loads
$(function(){
  var str = '#len'; //increment by 1 up to 1-nelemnts
  $(document).ready(function(){
    var i, stop;
    i = 1;
    stop = 4; //num elements
    setInterval(function(){
      if (i > stop){
        return;
      }
      $('#len'+(i++)).toggleClass('bounce');
    }, 200)
  });
});
