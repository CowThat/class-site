//Sort the tables
$('th').on('click', function(){
	var column = $(this).data('column')
	var order = $(this).data('order')
	var text = $(this).html()
	text = text.substring(0, text.length - 1)

	if(order == 'desc'){
		$(this).data('order', "asc")
		squishmallowTable = squishmallowTable.sort((a,b) => a[column] > b[column] ? 1 : -1)
		text += '&#9660'
	}else{
		$(this).data('order', "desc")
		squishmallowTable = squishmallowTable.sort((a,b) => a[column] < b[column] ? 1 : -1)
		text += '&#9650'
	}

	$(this).html(text)
	buildTable(squishmallowTable)

})


//Build the tables
buildTable(squishmallowTable)

function buildTable(data){
  var table = document.getElementById('squishmallowTable')

  table.innerHTML = ''

  for (var i = 0; i < data.length; i++){
    var row = `<tr class="view">
                <td><img src=\"photos/${data[i].img}.jpeg\"></img></td>
                <td>${data[i].name}</td>
                <td>${data[i].squishName}</td>
                <td>${data[i].animal}</td>
                <td>${data[i].sizeOwned}
                  <div class="hide">
                    Toggle-able content goes here.
                  </div>
               </td>

              </tr>`
    table.innerHTML += row
  }
}


